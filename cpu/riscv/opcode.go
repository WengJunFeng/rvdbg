package riscv

import "github.com/deadsy/rvdbg/util"

//-----------------------------------------------------------------------------

const matchJAL = 0x6f

//-----------------------------------------------------------------------------

func insJAL(rd, imm uint) uint32 {
	return uint32(matchJAL |
		(util.Bit(imm, 20) << 31) |
		(util.Bits(imm, 10, 1) << 21) |
		(util.Bit(imm, 11) << 20) |
		(util.Bits(imm, 19, 12) << 12) |
		(rd << 7))
}

